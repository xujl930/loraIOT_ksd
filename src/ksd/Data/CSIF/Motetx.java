package ksd.Data.CSIF;

public class Motetx {
	private boolean adr;
	private String codr;
	private String datr;
	private float freq;
	
	public boolean isAdr() {
		return adr;
	}
	public void setAdr(boolean adr) {
		this.adr = adr;
	}
	public String getCodr() {
		return codr;
	}
	public void setCodr(String codr) {
		this.codr = codr;
	}
	public String getDatr() {
		return datr;
	}
	public void setDatr(String datr) {
		this.datr = datr;
	}
	public float getFreq() {
		return freq;
	}
	public void setFreq(float freq) {
		this.freq = freq;
	}
	
	
	
}
